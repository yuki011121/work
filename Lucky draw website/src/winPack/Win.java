package winPack;

public class Win {

	private int rid;
	private int uid;
	private String win_time; //格式：yyyy-MM-dd hh:mm:ss
	
	public int getRid() {
		return rid;
	}
	
	public void setRid(int rid) {
		this.rid = rid;
	}
	
	public int getUid() {
		return uid;
	}
	
	public void setUid(int uid) {
		this.uid = uid;
	}
	
	public String getWin_time() {
		return win_time;
	}
	
	public void setWin_time(String win_time) {
		this.win_time = win_time;
	}
	
}
